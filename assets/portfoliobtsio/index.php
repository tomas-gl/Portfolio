                <div class="SIO">En quoi consiste le BTS SIO?</div>
                    <br>
                    Le brevet de technicien supérieur <i>(Services informatiques aux organisations)</i>
                    est un cursus d'études se déroulant sur deux
                    ans dans des lycées français, des écoles privées ou dans des centres de
                    formation professionnelle. Il peut être préparé en
                    formation continue, en alternance, par correspondance avec le Cned ou en formation initiale.
                    <br><br>
                    Dès le deuxième semestre de la première année, une option
                    doit être choisie par l'étudiant entre :
                    <br>
                    <b>SLAM </b><i>(Solutions Logicielles et Applications Métiers)</i>
                    : centrée sur la conception de programmes et logiciels de gestion
                    <br>
                    <b>SISR </b><i>(Solutions d'infrastructure, systèmes et réseaux)</i>
                    : tournée vers la gestion des infrastructures réseau d'un organisation
                    <br><br>
                    <div class="slam">L'option SLAM</div>
                    <br>
                    La spécialité <b>« Solutions Logicielles et Applications Métiers »</b> permet aux étudiants de réaliser des logiciels, des sites web,
                    de rédiger de la documentation liée à la solution applicative, de former des utilisateurs et de faire de la maintenance applicative,
                    <br><br>
                    À la fin de son BTS SIO spécialité SLAM, l’étudiant sera apte à devenir :
                    Analyste d’application, Analyste d’études, Analyste programmeur, Développeur d’applications informatiques,
                    Technicien d’études informatiques, Chargé d’études…
